    <p>Jacob Levy Moreno (nascut Iacob Levy, 18.05.1889 – 14.05.1974), nascut in Tara Romaneasca, in Bucuresti, a fost un psihiatru si psihosociolog Austro-American de renume, cugetator, educator, fondatorul psihodramei si pionierul psihoterapiei in grup.
        In timpul vietii sale, era considerat unul dintre oamenii de stiinta sociala de top.</p>
    <p>El a dezvoltat, in studiile sale asupra relatiilor dintre structurile sociale si bunastarea psihologica, conceptul de sociometrie, care reprezinta o metoda cantitativa de masurare a relatiilor sociale.</p>
    <p>Una dintre inovatiile lui Moreno in sociometrie este dezvoltarea sociogramei, o metoda sistematica de reprezentare grafica a indivizilor, prin noduri, si a relatiilor dintre ei, prin muchii.</p>
    <p>Prin intermediul website-ului prezent, dorim sa punem la dispozitia unui utilizator o platforma prin care se permite adaugarea si modificarea datele unui colectiv (grup de persoane), detectarea grupurilor mai mici din cadrul colectivului si reprezentarea vizuala a relatiilor interpersonale existente.</p>
